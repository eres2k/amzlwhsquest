// Helper functions
export function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function getRandomTaunt(type = 'bezos') {
  switch (type) {
    case 'bezos': return pick(BEZOS_TAUNTS);
    case 'yard': return pick(JEFF_YARD_TAUNTS);
    case 'castle': return pick(JEFF_CASTLE_TAUNTS);
    case 'simon': return pick(SIMULATED_AI.simon_taunts);
    default: return pick(SIMULATED_AI.taunts);
  }
}
